#include<unistd.h>
#include<stdio.h>
#include<string.h>
#include"libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (lst && new)
	{
		new->next = *lst;
		*lst = new;
	}
}
